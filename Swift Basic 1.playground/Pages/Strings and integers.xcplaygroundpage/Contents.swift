# String, Integers
 - - -
 Swift es un lenguage **Type-safe**, esto quiere decir que cada variable debe ser de un tipo **(datatype)** específico.
 
La variable saludo que creamos contiene un texto que dice "Hola Playground", por eso Swift le asigna el datatype **String**
 
*/
var saludo = "Hola playground!"
//:Por otro lado si queremos almacenar la edad de alguien tendremos una variable de la siguiente forma:
var edad = 20
//:Así almacenamos un número y Swift le asigna el tipo **Int**
var poblacion = 8_000_000
/*:String e Int son tipos diferentes por lo tanto no se pueden mezclar.
 
