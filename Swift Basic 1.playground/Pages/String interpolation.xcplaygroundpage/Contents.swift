# String interpolation
 - - -
 Es la habilidad que nos permite colocar variables dentro de los Strings, para hacerlos más útiles.
 
 Se puede poner cualquier tipo de dato dentro del string, solo debe ponerse la variable dentro de \\()
 
 También es posible correr código dentro.
 
*/

import Foundation

var puntaje = 70
var mensajePuntaje = "Tu puntaje fue \(puntaje)"

print(mensajePuntaje)

