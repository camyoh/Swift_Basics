# Operadores de comparación
 - - -
 Siwft tienen varios operadores que hacen comparación, y funcionan igual que en las matemáticas.
 
*/
let primerPuntaje = 6
let segundoPuntaje = 4
//:Existen dos operadores para verificar igualdad, == verifica que dos valores sean iguales y != verifica que dos valores no sean iguales.
primerPuntaje == segundoPuntaje
primerPuntaje != segundoPuntaje
//:Hay cuatro operadores para comparar si algún valor es más grande, menor o igual que otro.
primerPuntaje < segundoPuntaje
primerPuntaje >= segundoPuntaje
//:Esto también funciona con strings
let menor = "Alex" <= "Camilo"
