# Dictionaries
 - - -
 Son colecciones de valores como los array, pero en lugar de almacenar cosas en una posición de tipo entera, podemos acceder a ellos con el tipo de dato que queramos.
 Lo más común para almacernar data es usando strings.
*/
let alturas = [
    "Fabian": 1.68,
    "Davi": 1.63,
    "Juan": 1.77
]
//:Los idendificadores se llaman keys, y se pueden usar para leer el dato del diccionario asi:
alturas["Fabian"]
